export function getAllMechanics(): Mechanic[] {
  return MECHANIC_CATEGORIES.flatMap((cat) => cat.mechanics);
}

export function getMechanicById(id: string): Mechanic | undefined {
  return getAllMechanics().find((m) => m.id === id);
}

export function getMechanicsByCategory(categoryId: string): Mechanic[] {
  const cat = MECHANIC_CATEGORIES.find((c) => c.id === categoryId);
  return cat?.mechanics ?? [];
}

export function searchMechanics(query: string): Mechanic[] {
  const q = query.toLowerCase();
  return getAllMechanics().filter(
    (m) =>
      m.title.toLowerCase().includes(q) ||
      m.description.toLowerCase().includes(q) ||
      m.tags.some((t) => t.includes(q)) ||
      m.category.toLowerCase().includes(q)
  );
}
